const BASE_VALUES: [i64; 6] = [
    100,
    310,
    320,
    500,
    900,
    20000
];

pub fn base_value(p: PieceType) -> i64 {
    BASE_VALUES[p as usize]
}

pub fn bonus(i: usize, piece: Pc) -> i64 {

    let idx = match piece {
        Pc (Black, _) => i,
        Pc (White, _) => (7-(i/8))*8 + i % 8,
    };

    let Pc(_, k) = piece;
    let lookup = match k {
        Pawn => PAWN,
        Knight => KNIGHT,
        Bishop => BISHOP,
        Rook => ROOK,
        Queen => QUEEN,
        King => KING
    };

    lookup[idx]
}
